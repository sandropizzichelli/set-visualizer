function buildForteNumberLookup(keys) {
  const lookup = new Map();

  keys.forEach((key) => {
    const suffix = key.split("-")[1]?.replace("Z", "");
    if (!suffix) return;
    lookup.set(suffix, key);
  });

  return lookup;
}

function resolveGenusKeys(numbers, lookup) {
  return numbers
    .map((number) => lookup.get(String(number)))
    .filter(Boolean);
}

